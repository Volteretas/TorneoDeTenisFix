    private void jbBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbBuscarActionPerformed
        // TODO add your handling code here:
        try{
            Conexion conexion = new Conexion();
            JugadorData jd = new JugadorData(conexion);
            Jugador j = jd.obtenerJugador(Integer.parseInt(jtID.getText()));
            int index = 0;
            
            if("Agresivo".equals(j.getEstilo())){
                index = 0;
            }else if ("Voleadores".equals(j.getEstilo())){
                index = 1;
            }else if ("Toda la cancha".equals(j.getEstilo())){
                index = 2;
            }
            
            
            jtNombre.setText(j.getNombre());
            jtApellido.setText(j.getApellido());
            jtDNI.setText(String.valueOf(j.getDni()));
            jdFecha.setDate(java.sql.Date.valueOf(j.getFecha_nac()));
            jsAltura.setValue(j.getAltura());
            jsPeso.setValue(j.getPeso());
            jcEstilo.setSelectedIndex(index);
            
            if(j.isDiestro()){
                jrDiestro.setSelected(true);
            }else{
                jrZurdo.setSelected(true);
            }
            
        }catch(ClassNotFoundException ex){
            JOptionPane.showMessageDialog(null,"Error en la conexion " + ex);
        }catch(NullPointerException ex) {
            JOptionPane.showMessageDialog(null,"Ingrese datos validos" + ex);
        }
       
        activarBotonEditar();
        activarBotonBuscar();
    }//GEN-LAST:event_jbBuscarActionPerformed

    private void jbEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbEditarActionPerformed
        // TODO add your handling code here:
        try{
            Conexion conexion = new Conexion();
            JugadorData jd = new JugadorData(conexion);
            Date date = jdFecha.getDate();
            boolean diestro = true;
            LocalDate local = date.toInstant().atZone(ZoneId.systemDefault()).toLocalDate();
            if(jrDiestro.isSelected()){
                diestro = true;
            }else if(jrZurdo.isSelected()){
                diestro = false;
            }
            Jugador j = new Jugador(Integer.parseInt(jtID.getText()), jtNombre.getText(), jtApellido.getText(),Integer.parseInt(jtDNI.getText()), local,(double)jsAltura.getValue(),(double)jsPeso.getValue(), jcEstilo.getSelectedItem().toString(), diestro);
            
            jd.actualizarJugador(j);
            
        }catch(ClassNotFoundException ex){
            JOptionPane.showMessageDialog(null, "Error en la conexion " + ex);
        }
    }//GEN-LAST:event_jbEditarActionPerformed

