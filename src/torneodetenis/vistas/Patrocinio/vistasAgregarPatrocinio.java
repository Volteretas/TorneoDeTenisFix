    private void agregarItemJugadores(){
        try{
            Conexion conexion = new Conexion();
            JugadorData jd = new JugadorData(conexion);
            
            ArrayList<Jugador> jugadores = (ArrayList<Jugador>) jd.obtenerJugadores();
        
            for(int i = 0; i < jugadores.size(); i++){
                jcJugador.addItem(jugadores.get(i));
            }
        
        }catch(ClassNotFoundException ex){
            JOptionPane.showMessageDialog(null, "Error en la conexion " + ex);
        }     
    }
    
    private void agregarItemPatrocinador(){
        try{
            Conexion conexion = new Conexion();
            PatrocinioData pd = new PatrocinioData(conexion);
            JugadorData jd = new JugadorData(conexion);
            Jugador j = (Jugador) jcJugador.getSelectedItem();
        
            
            ArrayList<Patrocinador> patrocinadores = (ArrayList<Patrocinador>) pd.obtenerPatrociniosPosibles(j.getId_jugador());
        
            for(int i = 0; i < patrocinadores.size(); i++){
                jcPatrocinadores.addItem(patrocinadores.get(i));
            }
        
        }catch(ClassNotFoundException ex){
            JOptionPane.showMessageDialog(null, "Error en la conexion " + ex);
        }     
    }
    
    

