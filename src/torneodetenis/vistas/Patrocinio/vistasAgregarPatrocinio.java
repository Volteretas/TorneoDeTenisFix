    private void jbPatrocinarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbPatrocinarActionPerformed
        // TODO add your handling code here:
        try{
            Conexion conexion = new Conexion();
            JugadorData jd = new JugadorData(conexion);
            PatrocinadorData pd = new PatrocinadorData(conexion);
            PatrocinioData pcd = new PatrocinioData(conexion);
            
            Jugador j = (Jugador) jcJugador.getSelectedItem();
            Patrocinador p = (Patrocinador) jcPatrocinadores.getSelectedItem();
            Patrocinio pa = new Patrocinio(j, p);
            RankingData rd = new RankingData(conexion);
            
            pcd.agregarPatrocinio(pa);
            ArrayList<Ranking> rankings = (ArrayList<Ranking>) rd.obtenerRanking();
                    
            for(int i = 0; i < rankings.size(); i++){
                if(rankings.get(i).getJugador().getId_jugador() == j.getId_jugador()){
                    rd.sumarPuntos(rankings.get(i), 1);
                }
            }
            
        }catch(ClassNotFoundException ex){
            JOptionPane.showMessageDialog(null, "Error en la conexion " + ex);
        }
        
        jcPatrocinadores.removeAllItems();
        this.agregarItemPatrocinador();
    }//GEN-LAST:event_jbPatrocinarActionPerformed

