    private void jbBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbBuscarActionPerformed
        // TODO add your handling code here:
        try{
            Conexion conexion = new Conexion();
            EstadioData ed = new EstadioData(conexion);
            Estadio e = ed.obtenerEstadio(Integer.parseInt(jtID.getText()));
            jtCiudad.setText(e.getCiudad());
            jtDireccion.setText(e.getDireccion());
            jtEstado.setText(e.getEstado());
            jtNrident.setText(String.valueOf(e.getNumero_ident()));
            jsAncho.setValue(e.getAncho());
            jsLargo.setValue(e.getLargo());
            jsCantidad.setValue(e.getCantidad_espectadores());
            
        }catch(ClassNotFoundException ex){
            JOptionPane.showMessageDialog(null, "Error en la conexion " + ex);
        }catch(NullPointerException ne){
            JOptionPane.showMessageDialog(null, "Elemento no existe");
        }catch(NumberFormatException ex){
            JOptionPane.showMessageDialog(null, "ID ingresada no valido");
        }
    }//GEN-LAST:event_jbBuscarActionPerformed

