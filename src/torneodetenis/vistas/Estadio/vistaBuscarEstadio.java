    private void jbEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbEditarActionPerformed
        // TODO add your handling code here:
        try{
            Conexion conexion = new Conexion();
            EstadioData ed = new EstadioData(conexion);
            Estadio e1 = new Estadio(Integer.parseInt(jtID.getText()),Integer.parseInt(jtNrident.getText()), jtCiudad.getText(), jtDireccion.getText(), jtEstado.getText(), String.valueOf(jcCategoria.getSelectedItem()), (double) jsLargo.getValue(), (double) jsAncho.getValue(), (int) jsCantidad.getValue());
            
            ed.actualizarEstadio(e1);
            JOptionPane.showMessageDialog(null, "Se edito correctamente");
        }catch(ClassNotFoundException ex){
            JOptionPane.showMessageDialog(null, "Error en la conexion " + ex);
        }
    }//GEN-LAST:event_jbEditarActionPerformed

